# 整数

# 加法
print("2 + 3 =", 2 + 3)
# 减法
print("5 - 2 =", 5 - 2)
# 乘法
print("4 * 3 =", 4 * 3)
# 除法
print("8 / 2 =", 8 / 2)
# 整除
print("7 // 2 =", 7 // 2)
# 取模
print("9 % 4 =", 9 % 4)
# 幂运算
print("2 ** 3 =", 2 ** 3)

