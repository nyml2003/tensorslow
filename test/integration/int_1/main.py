# 大整数加法和乘法
a = 123456789012345678901234567890
b = 123456789012345678901234567890
print("大整数加法：", a + b)  # 输出两个大整数的和
print("大整数乘法：", a * b)  # 输出两个大整数的乘积

# 负数相加
a = -1
b = 1
print("负数相加：", a + b)  # 输出 -1 + 1 = 0

# 较大的整数相加和比较
a = 65536
b = 1
print("较大整数相加：", a + b)  # 输出 65536 + 1 = 65537
print("较大整数比较：", a > b)  # 输出 True，因为 65536 > 1

# 布尔逻辑运算
a = True
b = False
print("布尔逻辑与运算：", a and b)  # 输出 False，因为 True and False = False

# 按位运算示例
a = 10  # 二进制表示为 1010
b = 6   # 二进制表示为 0110
print("按位与运算（a & b）：", a & b)  # 输出 2，因为 1010 & 0110 = 0010
print("按位或运算（a | b）：", a | b)  # 输出 14，因为 1010 | 0110 = 1110
print("按位异或运算（a ^ b）：", a ^ b)  # 输出 12，因为 1010 ^ 0110 = 1100

# 乘方运算
a = 2
b = 3
print("乘方运算（a ** b）：", a ** b)  # 输出 2 的 3 次方，即 8

# 各种除法运算
a = 10
b = 3
print("整除（a // b）：", a // b)  # 输出 10 // 3 = 3，结果为整数
print("取模（a % b）：", a % b)  # 输出 10 % 3 = 1，余数

# 按位左移和右移运算
a = 8  # 二进制表示为 1000
b = 2

# 按位左移（<<）
print("按位左移（a << b）：", a << b)  # 输出 32，因为 1000 << 2 = 100000

# 按位右移（>>）
print("按位右移（a >> b）：", a >> b)  # 输出 2，因为 1000 >> 2 = 10
